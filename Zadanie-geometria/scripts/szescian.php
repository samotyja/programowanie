    <?php
    function pole($a, $b, $c)
    {
        return 6*(pow($a, 2));
    }
    function objentosc($a, $b,$c)
    {
        return pow($a, 3);
    }
    function dlugosc($a, $b, $c)
    {
        return $a*sqrt(3);
    }
    function promienwpis($a, $b, $c)
    {
        return (1/2)*$a;
    }
    function promienopis($a, $b, $c)
    {
        return (1/2) * $a * sqrt(3);
    }
