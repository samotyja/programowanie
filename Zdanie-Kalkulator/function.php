    <?php
    function dodawanie($wynik, $liczba)
    {
        return $wynik + $liczba;
    }
    function odejmowanie($wynik, $liczba)
    {
        return $wynik - $liczba;
    }
    function mnozenie($wynik, $liczba)
    {
        return $wynik * $liczba;
    }
    function dzielenie($wynik, $liczba)
    {
        return $wynik / $liczba;
    }

    ?>